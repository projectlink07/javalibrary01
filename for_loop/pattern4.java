package for_loop;

public class pattern4 {
    public static void main(String[] args) {
        for(int j = 5; j>= 1; j--){
            for(int i = j; i>= 1; i--){
                System.out.print(i);
            }
            System.out.println();
        }
        for(int k = 1; k<= 5; k++){
            for(int m=k; m> 0; m--){
                System.out.print(m);
            }
            System.out.println();
        }
    }
}
